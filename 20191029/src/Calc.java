
public class Calc {
	
	public void 덧셈(int a, int b) {
		int c = a + b;
		System.out.println(a + "+" + b + "=" + c );
	}
	public void 뺄셈(int a, int b) {
	int d = a - b;
	System.out.println(a + "-" + b + "=" + d );
	}
	public void 곱셈(int a, int b) {
	int e = a * b;
	System.out.println(a + "*" + b + "=" + e);
    }
	public void 나눗셈(int a, int b) {
		int f = a / b;
		System.out.println(a + "/" + b + "=" + f);
	}
	public void 나머지(int a, int b) {
		int g = a % b;
		System.out.println(a + "%" + b + "=" + g);
	}

}
